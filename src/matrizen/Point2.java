package matrizen;

/**
 *
 * @author Dominik
 */
public class Point2 {
    
    public final double x;
    public final double y;
    
    /**
     * Erzeugt ein Point2-Objekt, welches zwei double Werte speichert.
     * @param x x-Wert
     * @param y y-Wert
     */
    public Point2(final double x, final double y){
        this.x = x;
        this.y = y;
    }
    
}
